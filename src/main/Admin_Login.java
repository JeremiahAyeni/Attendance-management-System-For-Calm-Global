    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
        // TODO add your handling code here:
        String username = usernameField.getText();
        char[] password = passwordField.getPassword();

        String stringedPassword = String.copyValueOf(password);

        if (!username.isEmpty() && !stringedPassword.isEmpty()) {
            System.err.println(username + " " + stringedPassword);
            checkLoginDetails(username, stringedPassword);
        } else {
            invalidInfo.setForeground(Color.RED);
            invalidInfo.setText("*Fill Username and Password");
        }
    }//GEN-LAST:event_loginMouseClicked

