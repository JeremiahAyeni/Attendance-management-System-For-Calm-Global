    private void checkLoginDetails(String f, String s) {
        db.connect();

        try {
            String queryString = "SELECT USERNAME, PASSWORD FROM ADMIN where USERNAME=? and PASSWORD=?";
            db.ps = db.c.prepareStatement(queryString);

            db.ps.setString(1, f);
            db.ps.setString(2, s);

            try (ResultSet results = db.ps.executeQuery()) {
                if (results.next()) {
                    //JOptionPane.showMessageDialog(null, "Username and Password exist");
                    new Admin_Home().setVisible(true);
                    this.dispose();
                } else {
                    invalidInfo.setText("*Incorrect Username or Password");

                    //JOptionPane.showMessageDialog(null, "Please Check Username and Password ");
                }
            }
            db.c.close();

        } catch (SQLException err) {
            System.out.println(err);

        }
    }

